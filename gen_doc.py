def H(text, level=1):
    hd = doc.add_heading(text, level=level)
    for r in hd.runs:
        r.font.name = '宋体'
    return hd

def P(text, bold=False, size=None):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = '宋体'
    if bold:
        run.bold = True
    if size:
        run.font.size = Pt(size)
    return p

def T(text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(0.75)
    run = p.add_run(text)
    run.font.size = Pt(9)
    run.font.name = '宋体'
    run.font.color.rgb = RGBColor(0x6B, 0x72, 0x80)
    return p

def B(text):
    return doc.add_paragraph(text, style='List Bullet')

def M(headers, rows, w=None):
    tbl = doc.add_table(rows=len(rows)+1, cols=len(headers))
    tbl.style = 'Table Grid'
    for i, hd in enumerate(headers):
        cell = tbl.cell(0, i)
        cell.text = hd
        for pr in cell.paragraphs:
            for rn in pr.runs:
                rn.bold = True
                rn.font.size = Pt(9)
    for ri, row in enumerate(rows, 1):
        for ci, val in enumerate(row):
            tbl.cell(ri, ci).text = str(val)
            for pr in tbl.cell(ri, ci).paragraphs:
                for rn in pr.runs:
                    rn.font.size = Pt(9)
    if w:
        for row in tbl.rows:
            for ci, ww in enumerate(w):
                if ci < len(row.cells):
                    row.cells[ci].width = Cm(ww)
    return tbl

